"""
ENUNCIADO (If e Else)

Duas variáveis sao definidas no código de exemplo: 'room', uma string que informa qual cômodo da casa estamos olhando, e 'area', a área desse cômodo.

"""
# Define variables
room = "kit"
area = 14.0

# if statement for room
if room == "kit" :
    print("looking around in the kitchen.")

# if statement for area
if area > 15 :
    print("big place!")
else :
    print("pretty small.")